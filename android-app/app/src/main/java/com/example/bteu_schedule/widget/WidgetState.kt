package com.example.bteu_schedule.widget

/**
 * A7.7: Состояния виджета
 * 
 * Виджет может находиться в одном из следующих состояний:
 * 1. Нормальное - показывает актуальные данные
 * 2. Нет данных - "Недоступно. Обнови расписание." + кнопка "Обновить"
 * 3. Обновление - анимация точек, лёгкий shimmer
 * 4. Неправильная группа - "Выберите группу в приложении" + кнопка "Открыть"
 */
enum class WidgetState {
    /**
     * 1. Нормальное состояние
     * Показывает актуальные данные (расписание, реплику ассистента)
     */
    NORMAL,
    
    /**
     * 2. Нет данных
     * Сообщение: "Недоступно. Обнови расписание."
     * Кнопка: "Обновить"
     */
    NO_DATA,
    
    /**
     * 3. Обновление
     * Анимация точек, лёгкий shimmer
     */
    LOADING,
    
    /**
     * 4. Неправильная группа
     * Сообщение: "Выберите группу в приложении"
     * Кнопка: "Открыть"
     */
    NO_GROUP
}

